#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		i;
	int		n;

	i = 0;
	n = 0;
	if (!s)
		return (0);
	while (s[n] != '\0')
		n++;
	str = (char *)malloc((sizeof(char) * n) + 1);
	if (!str)
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f((char)s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
