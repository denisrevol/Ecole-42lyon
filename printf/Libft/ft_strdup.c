#include "libft.h"

char	*ft_strdup(const char *src)
{
	int		i;
	char	*str;
	int		cpt;

	cpt = ft_strlen(src);
	if (!(str = (char *)malloc(sizeof(*str) * (cpt + 1))))
		return (NULL);
	i = 0;
	while (src[i] != '\0')
	{
		str[i] = src[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
